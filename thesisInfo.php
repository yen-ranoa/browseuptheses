							<p id="abstract" style="font-size: 14px">The objectives of this experiment were 1) to directly compare the effects of two oligofructose models, a pulse dose and a staggered dose, on fecal indicators of post-ruminal acidosis and 2) to determine whether abomasal starch infusions result in similar changes to the oligofructose model. Five ruminally cannulated steers were fitted with abomasal infusion lines that were inserted through their ruminal cannulas. There were 5 treatments, a control (CON) of only water, a single pulse dose of 1g/kg of oligofructose (OL1) or starch (ST1) or 4 staggered doses of 0.25g/kg of oligofructose (OL4) or starch (ST4) administered every 6 h. Fecal samples were collected to measure fecal score, pH, dry matter (DM), VFA, endotoxin and apparent total tract nutrient digestibility. The results showed that a decrease in fecal pH from 7 to 6 occurred as a result of the OL1 and ST1 treatments and a decrease from 7 to 6.5 in the staggered doses. Fecal score also decreased from OL1 most severely but OL4 also caused a decline. An increase in total VFA and endotoxin concentrations in feces was seen across all treatments compared to CON. Both the pulse dose and staggered dose of oligofructose induced conditions similar to those seen in post-ruminal acidosis. Both starch treatments also showed similar effects to the oligofructose infusions suggesting that starch could be used as a model to induce post-ruminal acidosis.</p>
